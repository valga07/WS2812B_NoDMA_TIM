/**
  * @brief This function handles TIM1 capture compare interrupt.
  */
void TIM1_CC_IRQHandler(void)
{
  /* USER CODE BEGIN TIM1_CC_IRQn 0 */
	__HAL_TIM_CLEAR_IT(&htim1, TIM_IT_CC1);
	  //loads the next value or stops the timer
		if(PruebaLed.StopTimFlag)
		{
			HAL_TIM_PWM_Stop_IT(&htim1, TIM_CHANNEL_1);
			return;
		}
		//else, the value is loaded automatically from preloaded value
		if(!PruebaLed.bitCount)
		{
			if(!PruebaLed.ledCount)
			{
				htim1.Instance->CCR1 = 0;
				PruebaLed.StopTimFlag = 1;
				return;
			}
			else
			{
				PruebaLed.CurrentLed.RGB_Vals.ledRGB = *(PruebaLed.Array++);
				PruebaLed.ledCount--;
				PruebaLed.bitCount = 24;
			}
		}
			/*****Load next Value****/
			htim1.Instance->CCR1 = (PruebaLed.CurrentLed.ledVals & 0x8000000)? T1H:T0H;
			PruebaLed.CurrentLed.ledVals = PruebaLed.CurrentLed.ledVals << 1;
			PruebaLed.bitCount--;
  /* USER CODE END TIM1_CC_IRQn 0 */
  //HAL_TIM_IRQHandler(&htim1);
  /* USER CODE BEGIN TIM1_CC_IRQn 1 */

  /* USER CODE END TIM1_CC_IRQn 1 */
}

